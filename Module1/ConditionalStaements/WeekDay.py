NUM = int(input("Enter the number of the Day: "))
if NUM == 1:
    print("Sunday")
elif NUM == 2:
    print("Monday")
elif NUM == 3:
    print("Tuesday")
elif NUM == 4:
    print("Wednesday")
elif NUM == 5:
    print("Thursday")
elif NUM == 6:
    print("Friday")
elif NUM == 7:
    print("Saturday")
else:
    print("Invalid Day")